    boolean verifyPasswords()
    {
        char password1[]=jPasswordField.getPassword();
        
        if(password1.length<8)
        {
            lbPasswordInstruction.setText("Password length should be atleast 8 characters");
            lbPasswordInstruction.setVisible(true);
            return false;
        }
        if(password1.length>7)
        {
            lbPasswordInstruction.setVisible(false);
            return true;
        }  
        return false;
    }

