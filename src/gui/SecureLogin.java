    private void btLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLogInActionPerformed
        // TODO add your handling code here:
        String name = txtUserName.getText();
        String pass = jPasswordField.getText();
        if (name.equals("admin") && pass.equals("passwordhide")) {
            DataHideOption page = new DataHideOption();
            dispose();
            page.setVisible(true);
        } else if(name.equals("admin") && pass.equals("passwordextract")) {
            DataExtractOption page = new DataExtractOption();
            dispose();
            page.setVisible(true);
        }
        else
        {
            System.out.println("Please enter the valid username and password");
            JOptionPane.showMessageDialog(this, "Incorrect login or password", "Error", 0);  
        }
    }//GEN-LAST:event_btLogInActionPerformed

